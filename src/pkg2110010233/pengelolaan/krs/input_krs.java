    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    try {
        int row = tabel2.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diubah");
            return;
        }

        String npm = tabel2.getValueAt(row, 0).toString();
        String nama = txtnama.getText();
        String kelas = jComboBox1.getSelectedItem().toString();
        String jurusan = jComboBox2.getSelectedItem().toString();
        String semester = jComboBox3.getSelectedItem().toString();
        String dosen = txtdosen.getSelectedItem().toString();
        String matkul = txtmatkul.getText();
        String status = jComboBox5.getSelectedItem().toString();

        updateData(npm, nama, kelas, jurusan, semester, dosen, matkul, status);
        tampilkan(); // Refresh the table

    } catch (SQLException ex) {
        showErrorDialog("Error: " + ex.getMessage());
        Logger.getLogger(input_krs.class.getName()).log(Level.SEVERE, null, ex);
    }
}   

private void updateData(String npm, String nama, String kelas, String jurusan,
                         String semester, String dosen, String matkul, String status) throws SQLException {
    try (Connection cn = getConnection()) {
        String query = "UPDATE dtkrs SET nama=?, kelas=?, jurusan=?, "
                + "semester=?, dosen=?, matkul=?, status=? WHERE npm=?";
        try (PreparedStatement pst = cn.prepareStatement(query)) {
            pst.setString(1, nama);
            pst.setString(2, kelas);
            pst.setString(3, jurusan);
            pst.setString(4, semester);
            pst.setString(5, dosen);
            pst.setString(6, matkul);
            pst.setString(7, status);
            pst.setString(8, npm);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah");
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

