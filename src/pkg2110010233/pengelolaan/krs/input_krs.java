     private void tampilkan() {
        int row = model.getRowCount();
        for (int a = 0; a < row; a++) {
            model.removeRow(0);
        }

        try {
            Connection cn = getConnection();
            ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM dtkrs");

            while (rs.next()) {
                String data[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4),
                        rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(8)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            showErrorDialog("Koneksi ke database gagal: " + ex.getMessage());
            Logger.getLogger(input_krs.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private Connection getConnection() throws SQLException {
        return DriverManager.getConnection("jdbc:mysql://localhost/krs", "root", "");
    }

    private void insertData(String npm, String nama, String kelas, String jurusan,
                            String semester, String dosen, String matkul, String status) throws SQLException {
        try (Connection cn = getConnection()) {
            String query = "INSERT INTO dtkrs VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            try (PreparedStatement pst = cn.prepareStatement(query)) {
                pst.setString(1, npm);
                pst.setString(2, nama);
                pst.setString(3, kelas);
                pst.setString(4, jurusan);
                pst.setString(5, semester);
                pst.setString(6, dosen);
                pst.setString(7, matkul);
                pst.setString(8, status);

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
            }
        }
    }

    private void hapusData(String npm) throws SQLException {
        try (Connection cn = getConnection()) {
            String query = "DELETE FROM dtkrs WHERE npm = ?";
            try (PreparedStatement pst = cn.prepareStatement(query)) {
                pst.setString(1, npm);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
            }
        }
    }

    private void showErrorDialog(String errorMessage) {
        JOptionPane.showMessageDialog(this, errorMessage, "Error", JOptionPane.ERROR_MESSAGE);
    }
